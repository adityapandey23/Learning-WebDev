// while loop (condition check then execute)
let n = prompt("Enter the value of n: ");
n = Number.parseInt(n);
while(n>0){
    console.log(n);
    n--;
}

// do while loop (execute then condition check)
let m = prompt("Enter the value of m: ");
m = Number.parseInt(m);
do{
    console.log(m);
    m--;
}while(m>0);