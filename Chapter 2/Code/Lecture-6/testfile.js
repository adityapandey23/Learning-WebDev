// Arithmatic Operators in Javascript
console.log(7+6);
console.log(7-6);
console.log(7*6);
console.log(7/6); // will give a floating number
console.log(7**6);
let a = 10;
console.log(++a);
console.log(--a,"\n");


// Assignment Operators in Javascript
console.log(a);
a+=10
console.log(a);


// Comparision Operators in Javascript
let comp1 = 6;
let comp2 = 7;

console.log(comp1 == comp2);
console.log(comp1 != comp2);
console.log(comp1 === comp2); // Checks for the data type too!
console.log(comp1 !== comp2);
console.log("\n");


